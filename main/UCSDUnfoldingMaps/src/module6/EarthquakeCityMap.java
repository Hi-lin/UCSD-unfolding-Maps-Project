	/** Event handler that gets called automatically when the 
	 * mouse moves.
	 */
	public Marker lastAirport;
	@Override
	public void mouseMoved()
	{
		// clear the last selection
		if (lastSelected != null) {
			lastSelected.setSelected(false);
			lastSelected = null;
		
		}
		selectMarkerIfHover(quakeMarkers);
		selectMarkerIfHover(cityMarkers);
		
		//loop();
	}
	
	// If there is a marker selected 
	private void selectMarkerIfHover(List<Marker> markers)
	{
		// Abort if there's already a marker selected
		if (lastSelected != null) {
			return;
		}
		
		for (Marker m : markers) 
		{
			CommonMarker marker = (CommonMarker)m;
			if (marker.isInside(map,  mouseX, mouseY)) {
				lastSelected = marker;
				marker.setSelected(true);
				return;
			}
		}
	}
	
	/** The event handler for mouse clicks
	 * It will display an earthquake and its threat circle of cities
	 * Or if a city is clicked, it will display all the earthquakes 
	 * where the city is in the threat circle
	 */
	@Override
	public void mouseClicked()
	{
		int xbase = 25;
		int ybase = 50;
		if(mouseX>xbase+90&&mouseX<xbase+108&&mouseY>ybase+291&&mouseY<ybase+309) {
			Airports = Airports==false;
			for(Marker m: airportList) {
				m.setHidden(Airports);
			}
		}
		if (lastClicked != null) {
			unhideMarkers();
			((AirportMarker)lastAirport).track = false;
			lastAirport = null;
			for(Marker m:airportList) {
				((AirportMarker)m).dis = true;
			}
			lastClicked = null;
		}
		else if (lastClicked == null) 
		{
			checkEarthquakesForClick();
			if (lastClicked == null) {
				checkCitiesForClick();
			}
		}
		
	}
	
	private boolean drawlinks() {
		for(Marker m: airportList) {
			if(m.isInside(map, mouseX, mouseY)) {
				for(Marker g: airportList) {
					((AirportMarker)m).dis = false;
				}
				lastAirport = m;
				return true;
			}
		}
		return false;
	}
	private HashMap<String, Float> importDMI(String file){
		HashMap<String, Float> temp = new HashMap<String, Float>();
		String [] rows = loadStrings(file);
		for(String t: rows) {
			String col[] = t.split(",");
			try {
				Float i = Float.parseFloat(col[14]);
				temp.put(col[3], i);
			}
			catch(Exception e){
			}
		}
		return temp;
	}
	
	private void shadeCountries() {
		for(Marker m: countryMarkers) {
			String id = (String) m.getId();
			if(set.containsKey(id)) {
				float val = set.get(id);
				int colorl = (int) map(val,249, 80000, 10,255);
				m.setColor(color(255-colorl, 100, colorl));
			}
			else {
				m.setColor(color(155,155,155));
			}
		}
	}
	// Helper method that will check if a city marker was clicked on
	// and respond appropriately
	private void checkCitiesForClick()
	{
		if (lastClicked != null) return;
		// Loop over the earthquake markers to see if one of them is selected
		for (Marker marker : cityMarkers) {
			if (!marker.isHidden() && marker.isInside(map, mouseX, mouseY)) {
				lastClicked = (CommonMarker)marker;
				// Hide all the other earthquakes and hide
				for (Marker mhide : cityMarkers) {
					if (mhide != lastClicked) {
						mhide.setHidden(true);
					}
				}
				for (Marker mhide : quakeMarkers) {
					EarthquakeMarker quakeMarker = (EarthquakeMarker)mhide;
					if (quakeMarker.getDistanceTo(marker.getLocation()) 
							> quakeMarker.threatCircle()) {
						quakeMarker.setHidden(true);
					}
				}
				return;
			}
		}		
	}
	
	// Helper method that will check if an earthquake marker was clicked on
	// and respond appropriately
	private void checkEarthquakesForClick()
	{
		if (lastClicked != null) return;
		// Loop over the earthquake markers to see if one of them is selected
		for (Marker m : quakeMarkers) {
			EarthquakeMarker marker = (EarthquakeMarker)m;
			if (!marker.isHidden() && marker.isInside(map, mouseX, mouseY)) {
				lastClicked = marker;
				// Hide all the other earthquakes and hide
				for (Marker mhide : quakeMarkers) {
					if (mhide != lastClicked) {
						mhide.setHidden(true);
					}
				}
				for (Marker mhide : cityMarkers) {
					if (mhide.getDistanceTo(marker.getLocation()) 
							> marker.threatCircle()) {
						mhide.setHidden(true);
					}
				}
				return;
			}
		}
	}
	
	// loop over and unhide all markers
	private void unhideMarkers() {
		for(Marker marker : quakeMarkers) {
			marker.setHidden(false);
		}
			
		for(Marker marker : cityMarkers) {
			marker.setHidden(false);
		}
	}
	
	
